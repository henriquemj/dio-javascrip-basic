var divisaoInteira = 5 % 2;
console.log(divisaoInteira);

// pontenciação
var pontenciacao = 2 ** 10;
console.log(pontenciacao);

// maior que
var maiorQue = 5 > 2;
console.log(maiorQue); 

// menor que
var menorQue = 5 < 2;
console.log(menorQue);

